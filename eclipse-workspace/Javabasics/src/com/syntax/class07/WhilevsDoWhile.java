package com.syntax.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		int num=6;
		while(num<=5) {
			System.out.println("hello");
			num++;
		}
		System.out.println("==============DO WHILE LOOPS");
		int num1=6;//hello 5 times
		do{
		System.out.println("Hello");
		num1++;
		}while(num1<=5);
			
		
	}

}
