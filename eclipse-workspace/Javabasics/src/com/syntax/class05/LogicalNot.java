public class LogicalNot {

	public static void main(String[] args) {
	//not(!)-reverse the condition
		
		boolean b=!true;
	boolean val=!false;
	System.out.println(b);
    System.out.println(val);
	boolean isCold=true;
	if(!isCold) {
		System.out.println("hello");
	}else {
		System.out.println("bye");
	}
	
	}
	

}
