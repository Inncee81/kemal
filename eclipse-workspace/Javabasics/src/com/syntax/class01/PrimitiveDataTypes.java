package com.syntax.class01;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		//  8 primitive data types
		//to store whole numbers--byte,short,integer,long
		byte variableOne=127;
		short variable2=32767;
		int variable3=1000;
		long variable4=20000;
		//to store decimal numbers --float,double
		float variablefive=12.99f;
		double anyname=132.99;
		//when you want to store single character --char
		char singlevalue='f';
		char anothervariable='d';
		//to store boolean values--boolean
		boolean hungry=true;
		boolean thirsty=false;
		
		System.out.println();
		
	}
	}


